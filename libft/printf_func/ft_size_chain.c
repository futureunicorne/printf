#include "../libft.h"

int ft_length_chain(char *s)
{
	char *s1;
	int i;
	int j;

	i = 0;
	s1 = ft_strnew(2);
	while (s[i] != '\0')
	{
		if (s[i] >= 48 && s[i] <= 57)
		{
			j = 0;
			while (s[i] >= 48 && s[i] <= 57)
			{
				s1[j] = s[i];
				j++;
				i++;
			}
		}
		i++;
	}
	s1[j] = '\0';
	return (0);
}
